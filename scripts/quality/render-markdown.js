// Render the quality gate report as Markdown.
// Designed to be readable both as a PR comment and as a GitHub job summary.

const { formatPercent, formatDelta, safeNumber } = require("./utils");
const { typeMatchesSection } = require("./check-registry");

function statusLabel(status) {
  if (status === "passed") return "Passed";
  if (status === "warning") return "Warning";
  if (status === "failed") return "Failed";
  return "Unknown";
}

function row(label, result) {
  return `| ${label} | ${result} |`;
}

function categoryResult(report, section) {
  let blocking = 0;
  let warnings = 0;
  for (const r of report.regressions) if (typeMatchesSection(r.type, section)) blocking += 1;
  for (const w of report.warnings) if (typeMatchesSection(w.type, section)) warnings += 1;
  if (blocking > 0) return "Failed";
  if (warnings > 0) return "Warning";
  return "Passed";
}

function coverageTable(report) {
  const cur = (report.current && report.current.coverage && report.current.coverage.metrics) || {};
  const base = (report.baseline && report.baseline.coverage) || {};
  const metrics = ["lines", "statements", "functions", "branches"];
  const lines = [];
  lines.push("| Metric | Baseline | Current | Delta |");
  lines.push("|---|---:|---:|---:|");
  for (const m of metrics) {
    const c = safeNumber(cur[m]);
    const b = safeNumber(base[m]);
    const delta = c !== null && b !== null ? Math.round((c - b) * 100) / 100 : null;
    lines.push(
      `| ${m.charAt(0).toUpperCase() + m.slice(1)} | ${formatPercent(b)} | ${formatPercent(c)} | ${
        delta === null ? "n/a" : formatDelta(delta)
      } |`,
    );
  }
  return lines.join("\n");
}

function auditTable(report) {
  const counts = (report.current && report.current.audit && report.current.audit.counts) || {};
  const lines = [];
  lines.push("| Severity | Count |");
  lines.push("|---|---:|");
  for (const level of ["critical", "high", "moderate", "low", "info"]) {
    lines.push(`| ${level.charAt(0).toUpperCase() + level.slice(1)} | ${Number(counts[level]) || 0} |`);
  }
  return lines.join("\n");
}

function findingsBlock(title, items, opts = {}) {
  if (!items || items.length === 0) {
    if (opts.emptyText) return `### ${title}\n\n${opts.emptyText}\n`;
    return "";
  }
  const cap = opts.cap || 20;
  const lines = [`### ${title}\n`];
  for (const f of items.slice(0, cap)) {
    const msg = f.message || JSON.stringify(f);
    lines.push(`- ${msg}`);
  }
  if (items.length > cap) {
    lines.push(`- ...and ${items.length - cap} more (see reports/quality-gate.json).`);
  }
  return lines.join("\n") + "\n";
}

function renderMarkdown(report) {
  const lines = [];
  lines.push(`## Quality Gate\n`);
  lines.push(`**Status:** ${statusLabel(report.status)}\n`);

  lines.push(`### Summary\n`);
  lines.push("| Category | Result |");
  lines.push("|---|---|");
  lines.push(row("Coverage", categoryResult(report, "coverage")));
  lines.push(row("Audit", categoryResult(report, "audit")));
  lines.push(row("Duplication", categoryResult(report, "duplication")));
  lines.push(row("Lint", categoryResult(report, "eslint")));
  lines.push(row("File size", categoryResult(report, "files")));
  lines.push(row("Complexity", categoryResult(report, "complexity")));
  lines.push("");

  if (report.current && report.current.coverage && report.current.coverage.available) {
    lines.push(`### Coverage\n`);
    lines.push(coverageTable(report));
    lines.push("");
  }

  if (report.current && report.current.audit && report.current.audit.available) {
    lines.push(`### Dependency audit\n`);
    lines.push(auditTable(report));
    lines.push("");
  }

  lines.push(
    findingsBlock("Blocking regressions", report.regressions, {
      emptyText: "No blocking regressions found.",
    }),
  );

  lines.push(findingsBlock("Warnings", report.warnings, { cap: 15 }));

  if (report.recommendations && report.recommendations.length > 0) {
    lines.push("### Recommendations\n");
    for (const r of report.recommendations.slice(0, 10)) {
      lines.push(`- ${r}`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push(
    "_Generated by the [quality-gate](https://github.com/) skill. Deterministic checks are authoritative; AI commentary is advisory only._",
  );

  return lines.filter((s) => s !== undefined).join("\n").replace(/\n{3,}/g, "\n\n");
}

module.exports = {
  renderMarkdown,
  statusLabel,
};
